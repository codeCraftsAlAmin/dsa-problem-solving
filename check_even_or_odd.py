# Problem Statement:
# Check Even or Odd.
# Input: 4, 7
# Output: True, False
#
# Time Complexity:  O(1)
# Space Complexity: O(1)
# ============================================


def isEven(n):

    if n % 2 == 0:
        return True
    return False


print(isEven(25))
